package com.dicoding.javafundamental.perulangan;

public class For {
    public static void main(String[] args) {
        for (int i = 0; i < 3; i++) {
            System.out.println("Hello\n");
        }

        for (int i = 1; i < 11; i++) {
            System.out.println("Angka : "+ i);
        }
    }
}
